import SwiftUI

extension Font {
    static func onestBlack(size: CGFloat) -> Font {
        return Font.custom("Onest-Black", size: size)
    }
    static func onestBold(size: CGFloat) -> Font {
        return Font.custom("Onest-Bold", size: size)
    }
    static func onestExtraBold(size: CGFloat) -> Font {
        return Font.custom("Onest-ExtraBold", size: size)
    }
    static func onestExtraLight(size: CGFloat) -> Font {
        return Font.custom("Onest-ExtraLight", size: size)
    }
    static func onestLight(size: CGFloat) -> Font {
        return Font.custom("Onest-Light", size: size)
    }
    static func onestMedium(size: CGFloat) -> Font {
        return Font.custom("Onest-Medium", size: size)
    }
    static func onestRegular(size: CGFloat) -> Font {
        return Font.custom("Onest-Regular", size: size)
    }
    static func onestSemiBold(size: CGFloat) -> Font {
        return Font.custom("Onest-SemiBold", size: size)
    }
    static func onestThin(size: CGFloat) -> Font {
        return Font.custom("Onest-SemiBold", size: size)
    }
}
